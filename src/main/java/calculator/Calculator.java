package calculator;

public class Calculator {
    public static int plus(int i , int j) {
        return i + j;
    }

    public static int minus(int i, int j) {
        return i - j;
    }

    public static int multiply(int i, int j) {
        return i * j;
    }

    public static int divide(int i, int j) {
        return i / j;
    }
}
